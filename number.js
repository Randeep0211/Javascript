console.log(1 / 0); // Infinity
console.log(-1 / 0); // -Infinity
console.log(typeof 1 / 0); //NaN
console.log(typeof -1 / 0); // NaN
console.log(1.9999999999999999); // round of 2. 15 digit precise limit after decimal
